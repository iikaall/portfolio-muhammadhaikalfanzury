const getSpreadsheetUrl = () => {
  const config = getConfig();
  const url = String(config.spreadsheetCsvUrl || "").trim();
  if (!url) return "";
  if (!config.cacheBustSpreadsheet) return url;
  const separator = url.includes("?") ? "&" : "?";
  return `${url}${separator}v=${Date.now()}`;
};

const loadSpreadsheetData = async () => {
  const url = getSpreadsheetUrl();
  if (!url) return;

  try {
    const response = await fetch(url, { cache: "no-store" });
    if (!response.ok) throw new Error(`Spreadsheet request failed: ${response.status}`);
    const csv = await response.text();
    const sheetData = rowsToPortfolio(parseCsv(csv));
    renderPortfolio(mergePortfolio(sheetData));
  } catch (error) {
    console.warn("Portfolio spreadsheet could not be loaded. Fallback content is still visible.", error);
  }
};

navToggle?.addEventListener("click", () => {
  const isOpen = navLinks.classList.toggle("open");
  navToggle.setAttribute("aria-expanded", String(isOpen));
});

menuLinks.forEach((link) => {
  link.addEventListener("click", () => {
    navLinks.classList.remove("open");
    navToggle?.setAttribute("aria-expanded", "false");
  });
});

const setActiveLink = () => {
  const offset = header ? header.offsetHeight + 28 : 100;
  const activeSections = sections.filter((section) => section.getBoundingClientRect().top <= offset);
  const current = activeSections[activeSections.length - 1];

  menuLinks.forEach((link) => {
    const isActive = current && link.getAttribute("href") === `#${current.id}`;
    link.classList.toggle("active", Boolean(isActive));
  });
};

