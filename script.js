const parseCsv = (text) => {
  const rows = [];
  let row = [];
  let field = "";
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (char === '"' && inQuotes && next === '"') {
      field += '"';
      index += 1;
    } else if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === "," && !inQuotes) {
      row.push(field);
      field = "";
    } else if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(field);
      rows.push(row);
      row = [];
      field = "";
    } else {
      field += char;
    }
  }

  row.push(field);
  rows.push(row);

  const meaningfulRows = rows.filter((cells) => cells.some((cell) => String(cell).trim()));
  const headers = (meaningfulRows.shift() || []).map((header) =>
    String(header).trim().toLowerCase().replace(/\s+/g, "_")
  );

  return meaningfulRows.map((cells) =>
    headers.reduce((record, header, index) => {
      record[header] = String(cells[index] || "").trim();
      return record;
    }, {})
  );
};

const rowsToPortfolio = (rows) => {
  const data = {
    settings: {},
    stats: [],
    work: [],
    education: [],
    projects: [],
    projectMedia: [],
    projectHistory: [],
    skills: { professional: [], additional: [], language: [] },
    certifications: [],
    gallery: [],
    contacts: [],
    sourceSections: new Set(),
    sourceSkillGroups: new Set()
  };

  rows.forEach((row) => {
    const section = normalizeSection(row.section);
    if (section) data.sourceSections.add(section);
    if (section === "skill") data.sourceSkillGroups.add(normalizeSkillGroup(row.key));
    if (!isVisible(row.visible)) return;

    const order = Number(row.order || 9999);
    const title = firstValue(row.title, row.value);
    const description = firstValue(row.description, row.summary, row.ringkasan);

    if (section === "setting" && row.key) {
      data.settings[row.key] = firstValue(row.value, row.title, row.description, row.subtitle);
    }

    if (section === "stat") {
      data.stats.push({
        order,
        value: firstValue(row.value, row.title),
        label: firstValue(description, row.subtitle, row.title)
      });
    }

    if (section === "work") {
      data.work.push({
        order,
        period: row.period,
        title,
        subtitle: row.subtitle,
        description
      });
    }

    if (section === "education") {
      data.education.push({
        order,
        period: row.period,
        title,
        subtitle: row.subtitle,
        description
      });
    }

    if (section === "project") {
      data.projects.push({
        order,
        key: row.key || slugify(title),
        featured: parseBoolean(row.featured),
        tag: row.subtitle,
        title,
        description,
        bullets: splitList(row.bullets),
        tags: splitList(row.tags),
        image: row.image,
        alt: row.alt,
        linkLabel: row.link_label,
        linkUrl: row.link_url
      });
    }

    if (section === "projectMedia") {
      data.projectMedia.push({
        order,
        projectKey: row.key,
        type: row.subtitle || row.size,
        title,
        description,
        image: row.image,
        alt: row.alt,
        linkLabel: row.link_label,
        linkUrl: row.link_url,
        size: row.size
      });
    }

    if (section === "projectHistory") {
      data.projectHistory.push({
        order,
        period: row.period,
        title,
        subtitle: row.subtitle,
        description
      });
    }

    if (section === "skill") {
      const group = normalizeSkillGroup(row.key);
      const skill = firstValue(row.value, row.title);
      if (skill) data.skills[group].push({ order, skill });
    }

    if (section === "certification") {
      data.certifications.push({
        order,
        period: row.period,
        title,
        subtitle: row.subtitle,
        description,
        tags: splitList(row.tags),
        linkLabel: row.link_label,
        linkUrl: row.link_url
      });
    }

    if (section === "gallery") {
      data.gallery.push({
        order,
        key: row.key,
        type: row.subtitle || row.key,
        size: row.size || row.key,
        title,
        description,
        image: row.image,
        alt: row.alt,
        linkLabel: row.link_label,
        linkUrl: row.link_url
      });
    }

    if (section === "contact") {
      data.contacts.push({
        order,
        key: String(row.key || "").trim().toLowerCase(),
        title,
        value: firstValue(row.value, row.subtitle),
        linkUrl: row.link_url
      });
    }
  });

  data.stats = sortByOrder(data.stats).filter((item) => item.value && item.label);
  data.work = sortByOrder(data.work).filter((item) => item.title);
  data.education = sortByOrder(data.education).filter((item) => item.title);
  data.projects = sortByOrder(data.projects).filter((item) => item.title);
  data.projectMedia = sortByOrder(data.projectMedia).filter((item) => item.projectKey && (item.image || item.linkUrl));
  data.projectHistory = sortByOrder(data.projectHistory).filter((item) => item.title);
  data.certifications = sortByOrder(data.certifications).filter((item) => item.title);
  data.gallery = sortByOrder(data.gallery).filter((item) => item.title && (item.image || item.linkUrl));
  data.contacts = sortByOrder(data.contacts).filter((item) => item.title && item.value);

  Object.keys(data.skills).forEach((group) => {
    data.skills[group] = sortByOrder(data.skills[group]).map((item) => item.skill);
  });

  const mediaByProject = data.projectMedia.reduce((groups, item) => {
    const key = slugify(item.projectKey);
    if (!groups[key]) groups[key] = [];
    groups[key].push(item);
    return groups;
  }, {});

  data.projects = data.projects.map((project) => ({
    ...project,
    media: mediaByProject[slugify(project.key || project.title)] || []
  }));

  return data;
};

const hasSheetSection = (sheetData, section) => sheetData.sourceSections && sheetData.sourceSections.has(section);

const hasSheetSkillGroup = (sheetData, group) =>
  sheetData.sourceSkillGroups && sheetData.sourceSkillGroups.has(group);

const mergePortfolio = (sheetData) => ({
  settings: { ...fallbackData.settings, ...sheetData.settings },
  stats: hasSheetSection(sheetData, "stat") ? sheetData.stats : fallbackData.stats,
  work: hasSheetSection(sheetData, "work") ? sheetData.work : fallbackData.work,
  education: hasSheetSection(sheetData, "education") ? sheetData.education : fallbackData.education,
  projects: hasSheetSection(sheetData, "project") ? sheetData.projects : fallbackData.projects,
  projectHistory: hasSheetSection(sheetData, "projectHistory")
    ? sheetData.projectHistory
    : fallbackData.projectHistory,
  skills: {
    professional: hasSheetSkillGroup(sheetData, "professional")
      ? sheetData.skills.professional
      : fallbackData.skills.professional,
    additional: hasSheetSkillGroup(sheetData, "additional")
      ? sheetData.skills.additional
      : fallbackData.skills.additional,
    language: hasSheetSkillGroup(sheetData, "language") ? sheetData.skills.language : fallbackData.skills.language
  },
  certifications: hasSheetSection(sheetData, "certification") ? sheetData.certifications : fallbackData.certifications,
  gallery: hasSheetSection(sheetData, "gallery") ? sheetData.gallery : fallbackData.gallery,
  contacts: hasSheetSection(sheetData, "contact") ? sheetData.contacts : fallbackData.contacts
});

const getSpreadsheetUrl = () => {
  const config = getConfig();
  const url = String(config.spreadsheetCsvUrl || "").trim();
  if (!url) return "";
  if (!config.cacheBustSpreadsheet) return url;
  const separator = url.includes("?") ? "&" : "?";
  return `${url}${separator}v=${Date.now()}`;
};

const loadSpreadsheetData = async () => {
  const url = getSpreadsheetUrl();
  if (!url) return;

  try {
    const response = await fetch(url, { cache: "no-store" });
    if (!response.ok) throw new Error(`Spreadsheet request failed: ${response.status}`);
    const csv = await response.text();
    const sheetData = rowsToPortfolio(parseCsv(csv));
    renderPortfolio(mergePortfolio(sheetData));
  } catch (error) {
    console.warn("Portfolio spreadsheet could not be loaded. Fallback content is still visible.", error);
  }
};

